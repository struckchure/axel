// Package golang provides a built-in Go code generator for Axel.
// It generates Go structs from ASL types and typed query functions from AQL queries.
//
// Register via blank import in cmd/main.go:
//
//	import _ "github.com/struckchure/axel/generators/golang"
package golang

import (
	"bytes"
	"fmt"
	"go/format"
	"strings"

	"github.com/struckchure/axel/core/codegen"
)

func init() {
	codegen.Register(&GoGenerator{})
}

// GoGenerator is the built-in Go codegen plugin.
// It emits models.go (one struct per type) and queries.go (one function per AQL query).
type GoGenerator struct {
	models  bytes.Buffer
	queries bytes.Buffer

	pkgName     string
	currentType codegen.TypeDescriptor
}

func (g *GoGenerator) Name() string { return "go" }

// BeginSchema writes package header for both output files.
func (g *GoGenerator) BeginSchema(ctx *codegen.Context, _ codegen.SchemaDescriptor) error {
	g.pkgName = ctx.Options["package"]
	if g.pkgName == "" {
		g.pkgName = "generated"
	}
	g.models.Reset()
	g.queries.Reset()
	return nil
}

func (g *GoGenerator) OnScalar(_ *codegen.Context, _ codegen.ScalarDescriptor) error { return nil }
func (g *GoGenerator) OnEnum(_ *codegen.Context, e codegen.EnumDescriptor) error {
	// Emit a string-typed const block for each enum.
	fmt.Fprintf(&g.models, "type %s string\n\n", e.Name)
	fmt.Fprintf(&g.models, "const (\n")
	for _, v := range e.Values {
		fmt.Fprintf(&g.models, "\t%s%s %s = %q\n", e.Name, v, e.Name, v)
	}
	fmt.Fprintf(&g.models, ")\n\n")
	return nil
}

func (g *GoGenerator) BeginType(_ *codegen.Context, typ codegen.TypeDescriptor) error {
	g.currentType = typ
	fmt.Fprintf(&g.models, "type %s struct {\n", typ.Name)
	return nil
}

func (g *GoGenerator) OnProperty(_ *codegen.Context, p codegen.PropertyDescriptor) error {
	goType := aqlToGoType(p.AQLType, !p.IsRequired)
	fieldName := toGoFieldName(p.Name)
	fmt.Fprintf(&g.models, "\t%s %s `json:%q db:%q`\n", fieldName, goType, p.Name, p.Column)
	return nil
}

func (g *GoGenerator) OnLink(_ *codegen.Context, l codegen.LinkDescriptor) error {
	if l.IsMulti {
		// Multi-links are not direct struct fields (they live in junction tables).
		// Generators that want them can handle the LinkDescriptor themselves.
		return nil
	}
	// Single link — expose as the FK column value (string UUID).
	fieldName := toGoFieldName(l.Name + "_id")
	col := l.JoinColumn
	nullable := !l.IsRequired
	goType := "string"
	if nullable {
		goType = "*string"
	}
	fmt.Fprintf(&g.models, "\t%s %s `json:%q db:%q`\n", fieldName, goType, l.Name+"_id", col)
	return nil
}

func (g *GoGenerator) OnComputed(_ *codegen.Context, _ codegen.ComputedDescriptor) error {
	return nil
}
func (g *GoGenerator) OnIndex(_ *codegen.Context, _ codegen.IndexDescriptor) error { return nil }

func (g *GoGenerator) EndType(_ *codegen.Context) error {
	fmt.Fprintf(&g.models, "}\n\n")
	return nil
}

// EndSchema writes models.go and the queries.go header.
func (g *GoGenerator) EndSchema(ctx *codegen.Context) error {
	header := "// Code generated by axel codegen --generator go. DO NOT EDIT.\n\n"
	modelsImports := neededImports(g.models.String())

	var modelsSrc bytes.Buffer
	modelsSrc.WriteString(header)
	fmt.Fprintf(&modelsSrc, "package %s\n\n", g.pkgName)
	if len(modelsImports) > 0 {
		modelsSrc.WriteString("import (\n")
		for _, imp := range modelsImports {
			fmt.Fprintf(&modelsSrc, "\t%q\n", imp)
		}
		modelsSrc.WriteString(")\n\n")
	}
	modelsSrc.Write(g.models.Bytes())

	formatted, err := format.Source(modelsSrc.Bytes())
	if err != nil {
		// Write unformatted if gofmt fails (e.g. empty schema)
		formatted = modelsSrc.Bytes()
	}
	if err := ctx.WriteFile("models.go", formatted); err != nil {
		return err
	}
	return nil
}

// OnQuery emits a typed query function into queries.go.
func (g *GoGenerator) OnQuery(ctx *codegen.Context, q codegen.QueryDescriptor) error {
	var buf bytes.Buffer

	funcName := toGoExportedName(q.Name)
	paramsType := funcName + "Params"
	rowType := funcName + "Row"

	// Params struct
	if len(q.Params) > 0 {
		fmt.Fprintf(&buf, "type %s struct {\n", paramsType)
		for _, p := range q.Params {
			fmt.Fprintf(&buf, "\t%s %s\n", toGoExportedName(p.Name), aqlToGoType(p.AQLType, false))
		}
		fmt.Fprintf(&buf, "}\n\n")
	}

	// Result types
	var rowFields []codegen.ResultField
	switch {
	case q.Result.IsScalar:
		// count/aggregate — no row struct needed
	case q.Operation == "delete":
		// DELETE returns no rows
	default:
		rowFields = q.Result.Fields
		emitRowTypes(&buf, rowType, rowFields)
	}

	// Function signature
	switch q.Operation {
	case "select":
		if q.Result.IsScalar {
			emitScalarFunc(&buf, funcName, paramsType, q)
		} else if q.Result.IsMultiple {
			emitMultiRowFunc(&buf, funcName, paramsType, rowType, rowFields, q)
		} else {
			emitSingleRowFunc(&buf, funcName, paramsType, rowType, rowFields, q)
		}
	case "insert", "update":
		emitMutationFunc(&buf, funcName, paramsType, rowType, rowFields, q)
	case "delete":
		emitDeleteFunc(&buf, funcName, paramsType, q)
	}

	g.queries.Write(buf.Bytes())
	g.queries.WriteString("\n")

	// Flush queries.go on the last call — we do it here so we have all imports.
	// The CLI calls OnQuery for each file then Walk ends; we write after the loop
	// via a separate finalizeQueries call from EndSchema... but actually Walk calls
	// OnQuery after EndSchema. So we accumulate and write at the end of all OnQuery
	// calls. Since we don't have a "post-walk" hook, we write the file on every
	// OnQuery call, overwriting with the growing buffer. This is simple and correct.
	return g.flushQueries(ctx)
}

func (g *GoGenerator) flushQueries(ctx *codegen.Context) error {
	if g.queries.Len() == 0 {
		return nil
	}
	header := "// Code generated by axel codegen --generator go. DO NOT EDIT.\n\n"
	queriesImports := neededImports(g.queries.String())

	var src bytes.Buffer
	src.WriteString(header)
	fmt.Fprintf(&src, "package %s\n\n", g.pkgName)
	// Collect all needed imports for queries file.
	qs := g.queries.String()
	queryImportSet := map[string]bool{
		"context":      true,
		"database/sql": true,
	}
	for _, imp := range queriesImports {
		queryImportSet[imp] = true
	}
	if strings.Contains(qs, "errors.Is") {
		queryImportSet["errors"] = true
	}
	if strings.Contains(qs, "json.Unmarshal") {
		queryImportSet["encoding/json"] = true
	}
	src.WriteString("import (\n")
	for imp := range queryImportSet {
		fmt.Fprintf(&src, "\t%q\n", imp)
	}
	src.WriteString(")\n\n")
	src.Write(g.queries.Bytes())

	formatted, err := format.Source(src.Bytes())
	if err != nil {
		formatted = src.Bytes()
	}
	return ctx.WriteFile("queries.go", formatted)
}

// --- emit helpers ---

func emitRowTypes(buf *bytes.Buffer, rootName string, fields []codegen.ResultField) {
	fmt.Fprintf(buf, "type %s struct {\n", rootName)
	for _, f := range fields {
		fieldName := toGoExportedName(f.Name)
		if len(f.SubFields) > 0 {
			subType := rootName + toGoExportedName(f.Name)
			if f.IsMultiple {
				fmt.Fprintf(buf, "\t%s []%s `json:%q`\n", fieldName, subType, f.Name)
			} else {
				nullable := ""
				if f.IsNullable {
					nullable = "*"
				}
				fmt.Fprintf(buf, "\t%s %s%s `json:%q`\n", fieldName, nullable, subType, f.Name)
			}
		} else {
			goType := aqlToGoType(f.AQLType, f.IsNullable)
			fmt.Fprintf(buf, "\t%s %s `json:%q`\n", fieldName, goType, f.Name)
		}
	}
	fmt.Fprintf(buf, "}\n\n")

	// Emit nested sub-types
	for _, f := range fields {
		if len(f.SubFields) > 0 {
			emitRowTypes(buf, rootName+toGoExportedName(f.Name), f.SubFields)
		}
	}
}

func emitScalarFunc(buf *bytes.Buffer, funcName, paramsType string, q codegen.QueryDescriptor) {
	hasParams := len(q.Params) > 0
	sqlLit := backtick(q.SQL)
	paramArgs := buildParamArgs(q.Params)

	if hasParams {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB, params %s) (int64, error) {\n", funcName, paramsType)
	} else {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB) (int64, error) {\n", funcName)
	}
	fmt.Fprintf(buf, "\tconst query = %s\n", sqlLit)
	fmt.Fprintf(buf, "\tvar count int64\n")
	fmt.Fprintf(buf, "\terr := db.QueryRowContext(ctx, query%s).Scan(&count)\n", paramArgs)
	fmt.Fprintf(buf, "\treturn count, err\n")
	fmt.Fprintf(buf, "}\n")
}

func emitSingleRowFunc(buf *bytes.Buffer, funcName, paramsType, rowType string, fields []codegen.ResultField, q codegen.QueryDescriptor) {
	hasParams := len(q.Params) > 0
	sqlLit := backtick(q.SQL)
	paramArgs := buildParamArgs(q.Params)
	scanVars, scanTargets := buildScanVars(rowType, fields)

	if hasParams {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB, params %s) (*%s, error) {\n", funcName, paramsType, rowType)
	} else {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB) (*%s, error) {\n", funcName, rowType)
	}
	fmt.Fprintf(buf, "\tconst query = %s\n", sqlLit)
	fmt.Fprintf(buf, "\tvar r %s\n", rowType)
	if len(scanVars) > 0 {
		fmt.Fprintf(buf, "%s\n", scanVars)
	}
	fmt.Fprintf(buf, "\tif err := db.QueryRowContext(ctx, query%s).Scan(%s); err != nil {\n", paramArgs, scanTargets)
	fmt.Fprintf(buf, "\t\tif errors.Is(err, sql.ErrNoRows) {\n\t\t\treturn nil, nil\n\t\t}\n")
	fmt.Fprintf(buf, "\t\treturn nil, err\n")
	fmt.Fprintf(buf, "\t}\n")
	emitJSONUnmarshal(buf, fields)
	fmt.Fprintf(buf, "\treturn &r, nil\n")
	fmt.Fprintf(buf, "}\n")
}

func emitMultiRowFunc(buf *bytes.Buffer, funcName, paramsType, rowType string, fields []codegen.ResultField, q codegen.QueryDescriptor) {
	hasParams := len(q.Params) > 0
	sqlLit := backtick(q.SQL)
	paramArgs := buildParamArgs(q.Params)
	scanVars, scanTargets := buildScanVars(rowType, fields)

	if hasParams {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB, params %s) ([]%s, error) {\n", funcName, paramsType, rowType)
	} else {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB) ([]%s, error) {\n", funcName, rowType)
	}
	fmt.Fprintf(buf, "\tconst query = %s\n", sqlLit)
	fmt.Fprintf(buf, "\trows, err := db.QueryContext(ctx, query%s)\n", paramArgs)
	fmt.Fprintf(buf, "\tif err != nil {\n\t\treturn nil, err\n\t}\n")
	fmt.Fprintf(buf, "\tdefer rows.Close()\n")
	fmt.Fprintf(buf, "\tvar result []%s\n", rowType)
	fmt.Fprintf(buf, "\tfor rows.Next() {\n")
	fmt.Fprintf(buf, "\t\tvar r %s\n", rowType)
	if len(scanVars) > 0 {
		// indent scan vars inside loop
		for _, line := range strings.Split(strings.TrimRight(scanVars, "\n"), "\n") {
			fmt.Fprintf(buf, "\t%s\n", line)
		}
	}
	fmt.Fprintf(buf, "\t\tif err := rows.Scan(%s); err != nil {\n\t\t\treturn nil, err\n\t\t}\n", scanTargets)
	emitJSONUnmarshalIndented(buf, fields, "\t\t")
	fmt.Fprintf(buf, "\t\tresult = append(result, r)\n")
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\treturn result, rows.Err()\n")
	fmt.Fprintf(buf, "}\n")
}

func emitMutationFunc(buf *bytes.Buffer, funcName, paramsType, rowType string, fields []codegen.ResultField, q codegen.QueryDescriptor) {
	hasParams := len(q.Params) > 0
	sqlLit := backtick(q.SQL)
	paramArgs := buildParamArgs(q.Params)
	scanVars, scanTargets := buildScanVars(rowType, fields)

	if hasParams {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB, params %s) (*%s, error) {\n", funcName, paramsType, rowType)
	} else {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB) (*%s, error) {\n", funcName, rowType)
	}
	fmt.Fprintf(buf, "\tconst query = %s\n", sqlLit)
	fmt.Fprintf(buf, "\tvar r %s\n", rowType)
	if len(scanVars) > 0 {
		fmt.Fprintf(buf, "%s\n", scanVars)
	}
	fmt.Fprintf(buf, "\tif err := db.QueryRowContext(ctx, query%s).Scan(%s); err != nil {\n", paramArgs, scanTargets)
	fmt.Fprintf(buf, "\t\treturn nil, err\n")
	fmt.Fprintf(buf, "\t}\n")
	emitJSONUnmarshal(buf, fields)
	fmt.Fprintf(buf, "\treturn &r, nil\n")
	fmt.Fprintf(buf, "}\n")
}

func emitDeleteFunc(buf *bytes.Buffer, funcName, paramsType string, q codegen.QueryDescriptor) {
	hasParams := len(q.Params) > 0
	sqlLit := backtick(q.SQL)
	paramArgs := buildParamArgs(q.Params)

	if hasParams {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB, params %s) error {\n", funcName, paramsType)
	} else {
		fmt.Fprintf(buf, "func %s(ctx context.Context, db *sql.DB) error {\n", funcName)
	}
	fmt.Fprintf(buf, "\tconst query = %s\n", sqlLit)
	fmt.Fprintf(buf, "\t_, err := db.ExecContext(ctx, query%s)\n", paramArgs)
	fmt.Fprintf(buf, "\treturn err\n")
	fmt.Fprintf(buf, "}\n")
}

// buildScanVars returns:
//   - declarations for intermediate JSON byte slices (one per nested field)
//   - the scan target list: "&r.ID, &r.Email, &authorRaw, ..."
func buildScanVars(rowType string, fields []codegen.ResultField) (decls string, targets string) {
	var declBuf strings.Builder
	var targetParts []string
	for _, f := range fields {
		fieldName := toGoExportedName(f.Name)
		if len(f.SubFields) > 0 {
			varName := strings.ToLower(f.Name) + "Raw"
			fmt.Fprintf(&declBuf, "\tvar %s []byte\n", varName)
			targetParts = append(targetParts, "&"+varName)
		} else {
			targetParts = append(targetParts, "&r."+fieldName)
		}
	}
	_ = rowType
	return declBuf.String(), strings.Join(targetParts, ", ")
}

// emitJSONUnmarshal adds json.Unmarshal calls for nested JSON fields (single-row context).
func emitJSONUnmarshal(buf *bytes.Buffer, fields []codegen.ResultField) {
	emitJSONUnmarshalIndented(buf, fields, "\t")
}

func emitJSONUnmarshalIndented(buf *bytes.Buffer, fields []codegen.ResultField, indent string) {
	for _, f := range fields {
		if len(f.SubFields) > 0 {
			varName := strings.ToLower(f.Name) + "Raw"
			fieldName := toGoExportedName(f.Name)
			fmt.Fprintf(buf, "%sif len(%s) > 0 {\n", indent, varName)
			fmt.Fprintf(buf, "%s\tif err := json.Unmarshal(%s, &r.%s); err != nil {\n", indent, varName, fieldName)
			fmt.Fprintf(buf, "%s\t\treturn nil, err\n%s\t}\n%s}\n", indent, indent, indent)
		}
	}
}

// buildParamArgs builds the variadic args for db.QueryRowContext/db.QueryContext.
// Returns ", params.ID, params.Email" etc.
func buildParamArgs(params []codegen.ParamDescriptor) string {
	if len(params) == 0 {
		return ""
	}
	parts := make([]string, len(params))
	for i, p := range params {
		parts[i] = "params." + toGoExportedName(p.Name)
	}
	return ", " + strings.Join(parts, ", ")
}

// --- type helpers ---

// aqlToGoType maps an AQL type name to the appropriate Go type.
func aqlToGoType(aqlType string, nullable bool) string {
	base := map[string]string{
		"str":      "string",
		"int16":    "int16",
		"int32":    "int32",
		"int64":    "int64",
		"float32":  "float32",
		"float64":  "float64",
		"bool":     "bool",
		"uuid":     "string",
		"datetime": "time.Time",
		"date":     "time.Time",
		"time":     "time.Time",
		"json":     "json.RawMessage",
		"bytes":    "[]byte",
		"decimal":  "string",
	}[aqlType]

	if base == "" {
		base = "any"
	}

	// These types don't get pointer-ified for nullable
	if base == "json.RawMessage" || base == "[]byte" || base == "any" {
		return base
	}
	if nullable {
		return "*" + base
	}
	return base
}

// neededImports scans generated source for type references and returns needed imports.
func neededImports(src string) []string {
	var imports []string
	if strings.Contains(src, "time.Time") {
		imports = append(imports, "time")
	}
	if strings.Contains(src, "json.RawMessage") {
		imports = append(imports, "encoding/json")
	}
	return imports
}

// toGoFieldName converts a snake_case name to a Go exported PascalCase name.
func toGoFieldName(s string) string {
	return toGoExportedName(s)
}

// toGoExportedName converts snake_case → PascalCase.
func toGoExportedName(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if len(p) == 0 {
			continue
		}
		// Keep common acronyms uppercased
		upper := strings.ToUpper(p)
		if upper == "ID" || upper == "URL" || upper == "UUID" || upper == "API" || upper == "SQL" {
			parts[i] = upper
		} else {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	return strings.Join(parts, "")
}

// backtick wraps a string in backtick quotes, escaping any backticks inside.
func backtick(s string) string {
	// Go raw string literals can't contain backticks; use concatenation if needed.
	if !strings.Contains(s, "`") {
		return "`" + s + "`"
	}
	// Fall back to interpreted string literal
	s = strings.ReplaceAll(s, `\`, `\\`)
	s = strings.ReplaceAll(s, `"`, `\"`)
	s = strings.ReplaceAll(s, "\n", `\n`)
	s = strings.ReplaceAll(s, "\t", `\t`)
	return `"` + s + `"`
}
