// Package typescript provides a built-in TypeScript code generator for Axel.
// It generates TypeScript interfaces from ASL types, typed query functions from AQL queries,
// and a typed Runner class with a fluent query builder whose return type is inferred at
// compile time from the shape argument.
//
// Register via blank import in cmd/main.go:
//
//	import _ "github.com/struckchure/axel/generators/typescript"
package typescript

import (
	"bytes"
	"encoding/json"
	"fmt"
	"path/filepath"
	"strings"

	"github.com/struckchure/axel/core/codegen"
)

func init() {
	codegen.Register(&TsGenerator{})
}

// TsGenerator emits TypeScript models, typed query functions, and a typed Runner class.
type TsGenerator struct {
	models          bytes.Buffer
	queries         []codegen.QueryDescriptor
	schema          codegen.SchemaDescriptor
	currentAbstract bool
}

func (g *TsGenerator) Name() string { return "ts" }

func (g *TsGenerator) BeginSchema(_ *codegen.Context, schema codegen.SchemaDescriptor) error {
	g.models.Reset()
	g.queries = g.queries[:0]
	g.schema = schema
	g.currentAbstract = false
	return nil
}

func (g *TsGenerator) OnScalar(_ *codegen.Context, _ codegen.ScalarDescriptor) error { return nil }

func (g *TsGenerator) OnEnum(_ *codegen.Context, e codegen.EnumDescriptor) error {
	quoted := make([]string, len(e.Values))
	for i, v := range e.Values {
		quoted[i] = fmt.Sprintf("%q", v)
	}
	fmt.Fprintf(&g.models, "export type %s = %s;\n\n", e.Name, strings.Join(quoted, " | "))
	return nil
}

func (g *TsGenerator) BeginType(_ *codegen.Context, typ codegen.TypeDescriptor) error {
	g.currentAbstract = typ.IsAbstract
	if typ.IsAbstract {
		return nil
	}
	fmt.Fprintf(&g.models, "export interface %s {\n", typ.Name)
	return nil
}

func (g *TsGenerator) OnProperty(_ *codegen.Context, p codegen.PropertyDescriptor) error {
	if g.currentAbstract {
		return nil
	}
	tsType := aqlToTsType(p.AQLType, !p.IsRequired)
	fmt.Fprintf(&g.models, "  %s: %s;\n", toTsCamelCase(p.Name), tsType)
	return nil
}

func (g *TsGenerator) OnLink(_ *codegen.Context, l codegen.LinkDescriptor) error {
	if g.currentAbstract || l.IsMulti {
		return nil
	}
	tsType := "string"
	if !l.IsRequired {
		tsType = "string | null"
	}
	fmt.Fprintf(&g.models, "  %sId: %s;\n", toTsCamelCase(l.Name), tsType)
	return nil
}

func (g *TsGenerator) OnComputed(_ *codegen.Context, c codegen.ComputedDescriptor) error {
	if g.currentAbstract {
		return nil
	}
	// Computed field type is not stored in the descriptor; unknown is the safe default.
	fmt.Fprintf(&g.models, "  %s: unknown;\n", toTsCamelCase(c.Name))
	return nil
}

func (g *TsGenerator) OnIndex(_ *codegen.Context, _ codegen.IndexDescriptor) error { return nil }

func (g *TsGenerator) EndType(_ *codegen.Context) error {
	if g.currentAbstract {
		return nil
	}
	fmt.Fprintf(&g.models, "}\n\n")
	return nil
}

func (g *TsGenerator) OnQuery(ctx *codegen.Context, q codegen.QueryDescriptor) error {
	if err := g.emitQueryFile(ctx, q); err != nil {
		return err
	}
	g.queries = append(g.queries, q)
	return nil
}

func (g *TsGenerator) EndSchema(ctx *codegen.Context) error {
	if err := g.emitModels(ctx); err != nil {
		return err
	}
	return g.emitRunner(ctx)
}

// emitModels writes models.ts containing all interface and enum definitions.
func (g *TsGenerator) emitModels(ctx *codegen.Context) error {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by axel codegen --generator ts. DO NOT EDIT.\n\n")
	buf.Write(g.models.Bytes())
	return ctx.WriteFile("models.ts", buf.Bytes())
}

// emitQueryFile writes a single <query_name>.ts file with params interface, row interface,
// and a typed async function.
func (g *TsGenerator) emitQueryFile(ctx *codegen.Context, q codegen.QueryDescriptor) error {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by axel codegen --generator ts. DO NOT EDIT.\n\n")
	buf.WriteString("import type { DB } from \"./runner.ts\";\n\n")

	funcName := q.Name // already camelCase
	paramsTypeName := toUpperFirst(q.Name) + "Params"
	rowTypeName := toUpperFirst(q.Name) + "Row"
	hasParams := len(q.Params) > 0

	// Params interface
	if hasParams {
		fmt.Fprintf(&buf, "export interface %s {\n", paramsTypeName)
		for _, p := range q.Params {
			fmt.Fprintf(&buf, "  %s: %s;\n", toTsCamelCase(p.Name), aqlToTsType(p.AQLType, false))
		}
		buf.WriteString("}\n\n")
	}

	// Row interface
	switch q.Operation {
	case "select":
		if !q.Result.IsScalar && len(q.Result.Fields) > 0 {
			emitTsRowInterface(&buf, rowTypeName, q.Result.Fields)
		}
	case "insert", "update":
		if len(q.Result.Fields) > 0 {
			emitTsRowInterface(&buf, rowTypeName, q.Result.Fields)
		}
	}

	// Function
	sqlLit := escapeTsBacktick(q.SQL)
	switch q.Operation {
	case "select":
		if q.Result.IsScalar {
			emitTsFuncSig(&buf, funcName, hasParams, paramsTypeName, "Promise<number>")
			fmt.Fprintf(&buf, "  const query = `%s`;\n", sqlLit)
			if hasParams {
				fmt.Fprintf(&buf, "  const rows = await db.unsafe<{ count: number }>(query, [%s]);\n", buildTsParamArgs(q.Params))
			} else {
				buf.WriteString("  const rows = await db.unsafe<{ count: number }>(query);\n")
			}
			buf.WriteString("  return rows[0]?.count ?? 0;\n")
		} else if q.Result.IsMultiple {
			emitTsFuncSig(&buf, funcName, hasParams, paramsTypeName, fmt.Sprintf("Promise<%s[]>", rowTypeName))
			fmt.Fprintf(&buf, "  const query = `%s`;\n", sqlLit)
			if hasParams {
				fmt.Fprintf(&buf, "  return db.unsafe<%s>(query, [%s]);\n", rowTypeName, buildTsParamArgs(q.Params))
			} else {
				fmt.Fprintf(&buf, "  return db.unsafe<%s>(query);\n", rowTypeName)
			}
		} else {
			emitTsFuncSig(&buf, funcName, hasParams, paramsTypeName, fmt.Sprintf("Promise<%s | null>", rowTypeName))
			fmt.Fprintf(&buf, "  const query = `%s`;\n", sqlLit)
			if hasParams {
				fmt.Fprintf(&buf, "  const rows = await db.unsafe<%s>(query, [%s]);\n", rowTypeName, buildTsParamArgs(q.Params))
			} else {
				fmt.Fprintf(&buf, "  const rows = await db.unsafe<%s>(query);\n", rowTypeName)
			}
			buf.WriteString("  return rows[0] ?? null;\n")
		}
	case "insert", "update":
		emitTsFuncSig(&buf, funcName, hasParams, paramsTypeName, fmt.Sprintf("Promise<%s | null>", rowTypeName))
		fmt.Fprintf(&buf, "  const query = `%s`;\n", sqlLit)
		if hasParams {
			fmt.Fprintf(&buf, "  const rows = await db.unsafe<%s>(query, [%s]);\n", rowTypeName, buildTsParamArgs(q.Params))
		} else {
			fmt.Fprintf(&buf, "  const rows = await db.unsafe<%s>(query);\n", rowTypeName)
		}
		buf.WriteString("  return rows[0] ?? null;\n")
	case "delete":
		emitTsFuncSig(&buf, funcName, hasParams, paramsTypeName, "Promise<void>")
		fmt.Fprintf(&buf, "  const query = `%s`;\n", sqlLit)
		if hasParams {
			fmt.Fprintf(&buf, "  await db.unsafe(query, [%s]);\n", buildTsParamArgs(q.Params))
		} else {
			buf.WriteString("  await db.unsafe(query);\n")
		}
	}
	buf.WriteString("}\n")

	return ctx.WriteFile(queryTsFileName(q)+".ts", buf.Bytes())
}

// emitRunner writes runner.ts containing the DB interface, type helpers, builder classes,
// embedded schema JSON, and a typed Runner class with select/insert builders and
// one convenience method per compiled AQL query.
func (g *TsGenerator) emitRunner(ctx *codegen.Context) error {
	schemaJSON, err := json.Marshal(g.schema)
	if err != nil {
		return fmt.Errorf("marshaling schema: %w", err)
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by axel codegen --generator ts. DO NOT EDIT.\n\n")

	// Imports from per-query files
	for _, q := range g.queries {
		file := queryTsFileName(q)
		fn := q.Name
		pt := toUpperFirst(q.Name) + "Params"
		rt := toUpperFirst(q.Name) + "Row"
		hp := len(q.Params) > 0

		switch q.Operation {
		case "select":
			if q.Result.IsScalar {
				fmt.Fprintf(&buf, "import { %s } from \"./%s.ts\";\n", fn, file)
			} else if hp {
				fmt.Fprintf(&buf, "import { %s, type %s, type %s } from \"./%s.ts\";\n", fn, pt, rt, file)
			} else {
				fmt.Fprintf(&buf, "import { %s, type %s } from \"./%s.ts\";\n", fn, rt, file)
			}
		case "insert", "update":
			if hp {
				fmt.Fprintf(&buf, "import { %s, type %s, type %s } from \"./%s.ts\";\n", fn, pt, rt, file)
			} else {
				fmt.Fprintf(&buf, "import { %s, type %s } from \"./%s.ts\";\n", fn, rt, file)
			}
		case "delete":
			if hp {
				fmt.Fprintf(&buf, "import { %s, type %s } from \"./%s.ts\";\n", fn, pt, file)
			} else {
				fmt.Fprintf(&buf, "import { %s } from \"./%s.ts\";\n", fn, file)
			}
		}
	}

	// Import all non-abstract schema types from models.ts
	var typeNames []string
	for _, t := range g.schema.Types {
		if !t.IsAbstract {
			typeNames = append(typeNames, t.Name)
		}
	}
	if len(typeNames) > 0 {
		fmt.Fprintf(&buf, "import type { %s } from \"./models.ts\";\n", strings.Join(typeNames, ", "))
	}
	buf.WriteString("\n")

	// DB interface — compatible with Bun's SQL class (sql.unsafe) and any wrapper.
	buf.WriteString("export interface DB {\n")
	buf.WriteString("  unsafe<T = Record<string, unknown>>(sql: string, params?: unknown[]): Promise<T[]>;\n")
	buf.WriteString("}\n\n")

	// AxelSchema type map
	buf.WriteString("type AxelSchema = {\n")
	for _, name := range typeNames {
		fmt.Fprintf(&buf, "  %s: %s;\n", name, name)
	}
	buf.WriteString("};\n\n")

	// Shape / ShapeResult type helpers.
	// Shape<T>: boolean for scalar picks; SelectBuilder for correlated sub-selects (pass the
	// builder directly — do NOT call .all() on sub-select builders). The index signature covers
	// relationship keys that aren't on the model interface (e.g. `post` on User).
	buf.WriteString("// eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("type AnyBuilder = SelectBuilder<any, any> | FilterChain<any, any>;\n")
	buf.WriteString("// eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("export type Shape<T> = { [K in keyof T]?: boolean } & { [key: string]: boolean | AnyBuilder | undefined };\n\n")
	// ShapeResult iterates all keys of S so sub-select fields (not on T) are included.
	// - SelectBuilder<T2, S2> fields   → ShapeResult<T2, S2>[]  (resolved sub-select rows)
	// - boolean fields in keyof T      → T[K]                   (scalar model value)
	// - falsy / undefined values       → filtered out via `as` remapping
	buf.WriteString("export type ShapeResult<T, S> = {\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  [K in keyof S as S[K] extends (false | undefined | null) ? never : K]:\n")
	buf.WriteString("    // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("    S[K] extends SelectBuilder<infer T2, infer S2> ? ShapeResult<T2, S2>[] :\n")
	buf.WriteString("    // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("    S[K] extends FilterChain<infer T2, infer S2> ? ShapeResult<T2, S2>[] :\n")
	buf.WriteString("    K extends keyof T ? T[K] :\n")
	buf.WriteString("    never;\n")
	buf.WriteString("};\n\n")

	// FilterSpec — structured filter stored by where(); built into SQL at query time.
	buf.WriteString("interface FilterSpec {\n")
	buf.WriteString("  field: string;\n")
	buf.WriteString("  op:    string;\n")
	buf.WriteString("  value: unknown;\n")
	buf.WriteString("  /** true when value is a backtick-wrapped DB reference e.g. \"`User.id`\" */\n")
	buf.WriteString("  isRef: boolean;\n")
	buf.WriteString("  /** logical connector to the previous filter; ignored for the first filter */\n")
	buf.WriteString("  connector: \"and\" | \"or\";\n")
	buf.WriteString("}\n\n")

	// SelectBuilder
	buf.WriteString("class SelectBuilder<T, S extends Shape<T>> {\n")
	buf.WriteString("  _filterSpecs: FilterSpec[] = [];\n\n")
	buf.WriteString("  constructor(\n")
	buf.WriteString("    readonly db: DB,\n")
	buf.WriteString("    readonly _schema: Record<string, unknown>,\n")
	buf.WriteString("    readonly _typeName: string,\n")
	buf.WriteString("    readonly _shape: S,\n")
	buf.WriteString("  ) {}\n\n")
	buf.WriteString("  _addFilter(field: string, op: string, value: unknown, connector: \"and\" | \"or\"): void {\n")
	buf.WriteString("    const isRef = typeof value === \"string\" && value.startsWith(\"`\") && value.endsWith(\"`\");\n")
	buf.WriteString("    this._filterSpecs.push({ field, op, value: isRef ? (value as string).slice(1, -1) : value, isRef, connector });\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  /** Returns a FilterChain so .and()/.or() are only available after .where(). */\n")
	buf.WriteString("  where(field: keyof T & string, op: \"=\" | \"!=\" | \"<\" | \">\" | \"<=\" | \">=\", value: unknown): FilterChain<T, S> {\n")
	buf.WriteString("    this._addFilter(field, op, value, \"and\");\n")
	buf.WriteString("    return new FilterChain(this);\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  /** Internal: expose spec so _buildSubSelectSQL can read this builder's config. */\n")
	buf.WriteString("  _spec() {\n")
	buf.WriteString("    return { typeName: this._typeName, shape: this._shape as Record<string, unknown>, filterSpecs: this._filterSpecs };\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  async all(): Promise<ShapeResult<T, S>[]> {\n")
	buf.WriteString("    let subParamCount = 0;\n")
	buf.WriteString("    for (const [, val] of Object.entries(this._shape as Record<string, unknown>)) {\n")
	buf.WriteString("      // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("      if (typeof (val as any)?._spec === \"function\") subParamCount += (val as any)._spec().filterSpecs.filter((s: FilterSpec) => !s.isRef).length;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    const outerParams = this._filterSpecs.filter(s => !s.isRef).map(s => s.value);\n")
	buf.WriteString("    const sql = _buildSelectSQL(this._schema, this._typeName, this._shape as Record<string, unknown>, this._filterSpecs, subParamCount);\n")
	buf.WriteString("    return this.db.unsafe<ShapeResult<T, S>>(sql, outerParams);\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  async one(): Promise<ShapeResult<T, S> | null> {\n")
	buf.WriteString("    const rows = await this.all();\n")
	buf.WriteString("    return rows[0] ?? null;\n")
	buf.WriteString("  }\n")
	buf.WriteString("}\n\n")

	// FilterChain — returned by where(), exposes .and()/.or() chaining.
	buf.WriteString("class FilterChain<T, S extends Shape<T>> {\n")
	buf.WriteString("  constructor(private builder: SelectBuilder<T, S>) {}\n\n")
	buf.WriteString("  and(field: keyof T & string, op: \"=\" | \"!=\" | \"<\" | \">\" | \"<=\" | \">=\", value: unknown): this {\n")
	buf.WriteString("    this.builder._addFilter(field, op, value, \"and\");\n")
	buf.WriteString("    return this;\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  or(field: keyof T & string, op: \"=\" | \"!=\" | \"<\" | \">\" | \"<=\" | \">=\", value: unknown): this {\n")
	buf.WriteString("    this.builder._addFilter(field, op, value, \"or\");\n")
	buf.WriteString("    return this;\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  _spec() { return this.builder._spec(); }\n\n")
	buf.WriteString("  all(): Promise<ShapeResult<T, S>[]> { return this.builder.all(); }\n")
	buf.WriteString("  one(): Promise<ShapeResult<T, S> | null> { return this.builder.one(); }\n")
	buf.WriteString("}\n\n")

	// InsertBuilder
	buf.WriteString("type Insertable<T> = Partial<Omit<T, \"id\" | \"createdAt\" | \"updatedAt\">>;\n\n")
	buf.WriteString("class InsertBuilder<T> {\n")
	buf.WriteString("  constructor(\n")
	buf.WriteString("    private db: DB,\n")
	buf.WriteString("    private schema: Record<string, unknown>,\n")
	buf.WriteString("    private typeName: string,\n")
	buf.WriteString("    private values: Insertable<T>,\n")
	buf.WriteString("  ) {}\n\n")
	buf.WriteString("  async one(): Promise<T> {\n")
	buf.WriteString("    const { sql, params } = _buildInsertSQL(this.schema, this.typeName, this.values as Record<string, unknown>);\n")
	buf.WriteString("    const rows = await this.db.unsafe<T>(sql, params);\n")
	buf.WriteString("    return rows[0]!;\n")
	buf.WriteString("  }\n")
	buf.WriteString("}\n\n")

	// Embedded schema + SQL builder helpers
	fmt.Fprintf(&buf, "const _schema: Record<string, unknown> = JSON.parse(`%s`);\n\n", string(schemaJSON))

	buf.WriteString("function _toSnakeCase(s: string): string {\n")
	buf.WriteString("  return s.replace(/([A-Z])/g, (c) => `_${c.toLowerCase()}`);\n")
	buf.WriteString("}\n\n")

	buf.WriteString("function _buildSelectSQL(\n")
	buf.WriteString("  schema: Record<string, unknown>,\n")
	buf.WriteString("  typeName: string,\n")
	buf.WriteString("  shape: Record<string, unknown>,\n")
	buf.WriteString("  filterSpecs: FilterSpec[],\n")
	buf.WriteString("  paramOffset = 0,\n")
	buf.WriteString("): string {\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  const types = (schema as any).types as Array<{\n")
	buf.WriteString("    name: string;\n")
	buf.WriteString("    table: string;\n")
	buf.WriteString("    properties: Array<{ name: string; column: string }>;\n")
	buf.WriteString("    computed?: Array<{ name: string; expr: string }>;\n")
	buf.WriteString("    links?: Array<{ name: string; join_column: string }>;\n")
	buf.WriteString("  }>;\n")
	buf.WriteString("  const type = types.find((t) => t.name === typeName);\n")
	buf.WriteString("  if (!type) throw new Error(`axel: unknown type \"${typeName}\"`);\n\n")
	buf.WriteString("  const alias = typeName[0]!.toLowerCase();\n")
	buf.WriteString("  const cols: string[] = [];\n")
	buf.WriteString("  for (const [key, val] of Object.entries(shape)) {\n")
	buf.WriteString("    if (!val) continue;\n")
	buf.WriteString("    // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("    if (typeof (val as any)?._spec === \"function\") {\n")
	buf.WriteString("      // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("      cols.push(_buildSubSelectSQL(schema, (val as any)._spec(), key, alias));\n")
	buf.WriteString("      continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    const col = _toSnakeCase(key);\n")
	buf.WriteString("    const prop = type.properties?.find((p) => p.name === col || p.column === col);\n")
	buf.WriteString("    if (prop) { cols.push(`${alias}.\"${prop.column}\"`); continue; }\n")
	buf.WriteString("    const comp = type.computed?.find((c) => c.name === col);\n")
	buf.WriteString("    if (comp) cols.push(`(${comp.expr}) AS \"${col}\"`);\n")
	buf.WriteString("  }\n")
	buf.WriteString("  if (cols.length === 0) cols.push(`${alias}.*`);\n\n")
	buf.WriteString("  let paramIdx = paramOffset + 1;\n")
	buf.WriteString("  const filters: Array<{ expr: string; connector: \"and\" | \"or\" }> = [];\n")
	buf.WriteString("  for (const spec of filterSpecs) {\n")
	buf.WriteString("    const snakeField = _toSnakeCase(spec.field);\n")
	buf.WriteString("    const filterProp = type.properties?.find((p) => p.name === snakeField || p.column === snakeField);\n")
	buf.WriteString("    const filterLink = !filterProp && type.links?.find((l) =>\n")
	buf.WriteString("      l.name === snakeField || l.join_column === snakeField || l.name + \"_id\" === snakeField\n")
	buf.WriteString("    );\n")
	buf.WriteString("    const col = filterProp\n")
	buf.WriteString("      ? `${alias}.\"${filterProp.column}\"`\n")
	buf.WriteString("      : filterLink\n")
	buf.WriteString("        ? `${alias}.\"${filterLink.join_column}\"`\n")
	buf.WriteString("        : `${alias}.\"${snakeField}\"`;\n")
	buf.WriteString("    const expr = spec.isRef\n")
	buf.WriteString("      ? `${col} ${spec.op} ${_resolveOuterRef(schema, spec.value as string)}`\n")
	buf.WriteString("      : `${col} ${spec.op} $${paramIdx++}`;\n")
	buf.WriteString("    filters.push({ expr, connector: spec.connector });\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  let sql = `SELECT ${cols.join(\", \")} FROM \"${type.table}\" ${alias}`;\n")
	buf.WriteString("  if (filters.length > 0) {\n")
	buf.WriteString("    let where = filters[0]!.expr;\n")
	buf.WriteString("    for (let i = 1; i < filters.length; i++) {\n")
	buf.WriteString("      where += ` ${filters[i]!.connector.toUpperCase()} ${filters[i]!.expr}`;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    sql += ` WHERE ${where}`;\n")
	buf.WriteString("  }\n")
	buf.WriteString("  return sql;\n")
	buf.WriteString("}\n\n")

	// _buildSubSelectSQL — wraps an inner SelectBuilder spec as a correlated json_agg subquery.
	buf.WriteString("function _buildSubSelectSQL(\n")
	buf.WriteString("  schema: Record<string, unknown>,\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  spec: { typeName: string; shape: Record<string, unknown>; filterSpecs: FilterSpec[] },\n")
	buf.WriteString("  fieldName: string,\n")
	buf.WriteString("  _outerAlias: string,\n")
	buf.WriteString("): string {\n")
	buf.WriteString("  const innerSQL = _buildSelectSQL(schema, spec.typeName, spec.shape, spec.filterSpecs, 0);\n")
	buf.WriteString("  const subAlias = `${spec.typeName[0]!.toLowerCase()}_${fieldName}_sub`;\n")
	buf.WriteString("  return `(SELECT json_agg(row_to_json(${subAlias})) FROM (${innerSQL}) ${subAlias}) AS \"${fieldName}\"`;\n")
	buf.WriteString("}\n\n")

	// _resolveOuterRef — turns a "TypeName.field" string into a qualified SQL column reference.
	buf.WriteString("function _resolveOuterRef(schema: Record<string, unknown>, ref: string): string {\n")
	buf.WriteString("  const dotIdx = ref.indexOf(\".\");\n")
	buf.WriteString("  if (dotIdx === -1) return ref;\n")
	buf.WriteString("  const typeName = ref.slice(0, dotIdx);\n")
	buf.WriteString("  const fieldName = ref.slice(dotIdx + 1);\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  const types = (schema as any).types as Array<{\n")
	buf.WriteString("    name: string;\n")
	buf.WriteString("    properties?: Array<{ name: string; column: string }>;\n")
	buf.WriteString("    links?: Array<{ name: string; join_column: string }>;\n")
	buf.WriteString("  }>;\n")
	buf.WriteString("  const type = types.find((t) => t.name === typeName);\n")
	buf.WriteString("  if (!type) return ref;\n")
	buf.WriteString("  const alias = typeName[0]!.toLowerCase();\n")
	buf.WriteString("  const col = _toSnakeCase(fieldName);\n")
	buf.WriteString("  const prop = type.properties?.find((p) => p.name === col || p.column === col);\n")
	buf.WriteString("  if (prop) return `${alias}.\"${prop.column}\"`;\n")
	buf.WriteString("  const link = type.links?.find((l) => l.name === col);\n")
	buf.WriteString("  if (link) return `${alias}.\"${link.join_column}\"`;\n")
	buf.WriteString("  return `${alias}.\"${col}\"`;\n")
	buf.WriteString("}\n\n")

	buf.WriteString("function _buildInsertSQL(\n")
	buf.WriteString("  schema: Record<string, unknown>,\n")
	buf.WriteString("  typeName: string,\n")
	buf.WriteString("  values: Record<string, unknown>,\n")
	buf.WriteString("): { sql: string; params: unknown[] } {\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  const types = (schema as any).types as Array<{\n")
	buf.WriteString("    name: string;\n")
	buf.WriteString("    table: string;\n")
	buf.WriteString("    properties: Array<{ name: string; column: string }>;\n")
	buf.WriteString("  }>;\n")
	buf.WriteString("  const type = types.find((t) => t.name === typeName);\n")
	buf.WriteString("  if (!type) throw new Error(`axel: unknown type \"${typeName}\"`);\n\n")
	buf.WriteString("  const cols: string[] = [];\n")
	buf.WriteString("  const params: unknown[] = [];\n")
	buf.WriteString("  for (const [key, val] of Object.entries(values)) {\n")
	buf.WriteString("    const col = _toSnakeCase(key);\n")
	buf.WriteString("    const prop = type.properties?.find((p) => p.name === col || p.column === col);\n")
	buf.WriteString("    if (prop) {\n")
	buf.WriteString("      cols.push(`\"${prop.column}\"`);\n")
	buf.WriteString("      params.push(val);\n")
	buf.WriteString("    }\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  const placeholders = params.map((_, i) => `$${i + 1}`);\n")
	buf.WriteString("  const sql =\n")
	buf.WriteString("    \"BEGIN;\\n\" +\n")
	buf.WriteString("    `INSERT INTO \"${type.table}\" (${cols.join(\", \")}) VALUES (${placeholders.join(\", \")}) RETURNING *;` +\n")
	buf.WriteString("    \"\\nCOMMIT;\";\n")
	buf.WriteString("  return { sql, params };\n")
	buf.WriteString("}\n\n")

	// ─── AQL runtime: types ──────────────────────────────────────────────────────
	buf.WriteString("// ─── AQL runtime ────────────────────────────────────────────────────────────\n")
	buf.WriteString("interface _AQLField {\n")
	buf.WriteString("  name: string;\n")
	buf.WriteString("  subSelect?: _AQLSelect;\n")
	buf.WriteString("  subInsert?: _AQLInsert;\n")
	buf.WriteString("  value?: { kind: \"param\" | \"lit\"; v: string };\n")
	buf.WriteString("}\n")
	buf.WriteString("interface _AQLFilter { field: string; op: string; value: string; isParam: boolean; isRef: boolean; }\n")
	buf.WriteString("interface _AQLSelect { typeName: string; fields: _AQLField[]; filter?: _AQLFilter; }\n")
	buf.WriteString("interface _AQLInsert { typeName: string; fields: _AQLField[]; }\n")
	buf.WriteString("type _AQLStmt =\n")
	buf.WriteString("  | { kind: \"select\"; sel: _AQLSelect }\n")
	buf.WriteString("  | { kind: \"insert\"; ins: _AQLInsert }\n")
	buf.WriteString("  | { kind: \"delete\"; del: { typeName: string; filter?: _AQLFilter } };\n\n")

	// ─── AQL runtime: lexer ──────────────────────────────────────────────────────
	buf.WriteString("function _lexAQL(src: string): Array<{ k: string; v: string }> {\n")
	buf.WriteString("  const KWS = new Set([\"select\", \"insert\", \"update\", \"delete\", \"filter\", \"set\"]);\n")
	buf.WriteString("  const toks: Array<{ k: string; v: string }> = [];\n")
	buf.WriteString("  let i = 0;\n")
	buf.WriteString("  while (i < src.length) {\n")
	buf.WriteString("    if (src[i]! === \"#\") { while (i < src.length && src[i]! !== \"\\n\") i++; continue; }\n")
	buf.WriteString("    if (/\\s/.test(src[i]!)) { i++; continue; }\n")
	buf.WriteString("    if (src[i]! === \":\" && src.charAt(i + 1) === \"=\") { toks.push({ k: \"asgn\", v: \":=\" }); i += 2; continue; }\n")
	buf.WriteString("    if (src[i]! === \".\") { toks.push({ k: \"dot\", v: \".\" }); i++; continue; }\n")
	buf.WriteString("    if (src[i]! === \"$\") {\n")
	buf.WriteString("      let j = i + 1;\n")
	buf.WriteString("      while (j < src.length && /\\w/.test(src[j]!)) j++;\n")
	buf.WriteString("      toks.push({ k: \"param\", v: src.slice(i + 1, j) });\n")
	buf.WriteString("      i = j; continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    if (/[a-zA-Z_]/.test(src[i]!)) {\n")
	buf.WriteString("      let j = i;\n")
	buf.WriteString("      while (j < src.length && /\\w/.test(src[j]!)) j++;\n")
	buf.WriteString("      const w = src.slice(i, j);\n")
	buf.WriteString("      toks.push({ k: KWS.has(w) ? \"kw\" : \"id\", v: w });\n")
	buf.WriteString("      i = j; continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    if (/\\d/.test(src[i]!)) {\n")
	buf.WriteString("      let j = i;\n")
	buf.WriteString("      while (j < src.length && /[\\d.]/.test(src[j]!)) j++;\n")
	buf.WriteString("      toks.push({ k: \"num\", v: src.slice(i, j) });\n")
	buf.WriteString("      i = j; continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    if (src[i]! === \"'\" || src[i]! === '\"') {\n")
	buf.WriteString("      const q = src[i]!; let j = i + 1; let s = \"\";\n")
	buf.WriteString("      while (j < src.length && src[j]! !== q) {\n")
	buf.WriteString("        if (src[j]! === \"\\\\\") j++;\n")
	buf.WriteString("        s += src[j++]!;\n")
	buf.WriteString("      }\n")
	buf.WriteString("      toks.push({ k: \"str\", v: s });\n")
	buf.WriteString("      i = j + 1; continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    if (\"!=<>\".includes(src[i]!)) {\n")
	buf.WriteString("      let j = i;\n")
	buf.WriteString("      while (j < src.length && \"!=<>\".includes(src[j]!)) j++;\n")
	buf.WriteString("      toks.push({ k: \"op\", v: src.slice(i, j) });\n")
	buf.WriteString("      i = j; continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    toks.push({ k: \"p\", v: src[i]! });\n")
	buf.WriteString("    i++;\n")
	buf.WriteString("  }\n")
	buf.WriteString("  toks.push({ k: \"eof\", v: \"\" });\n")
	buf.WriteString("  return toks;\n")
	buf.WriteString("}\n\n")

	// ─── AQL runtime: parser ─────────────────────────────────────────────────────
	buf.WriteString("function _parseAQL(src: string): _AQLStmt {\n")
	buf.WriteString("  const toks = _lexAQL(src);\n")
	buf.WriteString("  let pos = 0;\n")
	buf.WriteString("  const peek = () => toks[pos]!;\n")
	buf.WriteString("  const eat = () => toks[pos++]!;\n")
	buf.WriteString("  const expect = (k: string, v?: string) => {\n")
	buf.WriteString("    const t = eat();\n")
	buf.WriteString("    if (t.k !== k || (v !== undefined && t.v !== v))\n")
	buf.WriteString("      throw new Error(\"axel/aql: expected \" + k + (v ? \"=\" + v : \"\") + \" got \" + JSON.stringify(t));\n")
	buf.WriteString("    return t;\n")
	buf.WriteString("  };\n")
	buf.WriteString("  const skip = (k: string, v?: string) => {\n")
	buf.WriteString("    if (peek().k === k && (v === undefined || peek().v === v)) { pos++; return true; }\n")
	buf.WriteString("    return false;\n")
	buf.WriteString("  };\n\n")
	buf.WriteString("  const parseFilter = (): _AQLFilter | undefined => {\n")
	buf.WriteString("    if (peek().k !== \"kw\" || peek().v !== \"filter\") return undefined;\n")
	buf.WriteString("    eat();\n")
	buf.WriteString("    expect(\"dot\");\n")
	buf.WriteString("    let field = expect(\"id\").v;\n")
	buf.WriteString("    while (peek().k === \"dot\") { eat(); field += \".\" + expect(\"id\").v; }\n")
	buf.WriteString("    const op = expect(\"op\").v;\n")
	buf.WriteString("    const vt = peek();\n")
	buf.WriteString("    let value = \"\"; let isParam = false; let isRef = false;\n")
	buf.WriteString("    if (vt.k === \"param\") { eat(); value = vt.v; isParam = true; }\n")
	buf.WriteString("    else if (vt.k === \"id\") {\n")
	buf.WriteString("      eat();\n")
	buf.WriteString("      if (peek().k === \"dot\") { eat(); value = vt.v + \".\" + expect(\"id\").v; isRef = true; }\n")
	buf.WriteString("      else value = vt.v;\n")
	buf.WriteString("    } else { eat(); value = vt.v; }\n")
	buf.WriteString("    return { field, op, value, isParam, isRef };\n")
	buf.WriteString("  };\n\n")
	buf.WriteString("  // forward declarations for mutual recursion\n")
	buf.WriteString("  let parseSelect: () => _AQLSelect;\n")
	buf.WriteString("  let parseInsert: () => _AQLInsert;\n\n")
	buf.WriteString("  const parseFields = (): _AQLField[] => {\n")
	buf.WriteString("    expect(\"p\", \"{\");\n")
	buf.WriteString("    const fields: _AQLField[] = [];\n")
	buf.WriteString("    while (!(peek().k === \"p\" && peek().v === \"}\")) {\n")
	buf.WriteString("      skip(\"p\", \",\"); skip(\"p\", \";\");\n")
	buf.WriteString("      if ((peek().k === \"p\" && peek().v === \"}\") || peek().k === \"eof\") break;\n")
	buf.WriteString("      const name = expect(\"id\").v;\n")
	buf.WriteString("      if (skip(\"asgn\")) {\n")
	buf.WriteString("        if (skip(\"p\", \"(\")) {\n")
	buf.WriteString("          if (peek().k === \"kw\" && peek().v === \"select\") {\n")
	buf.WriteString("            eat(); const sel = parseSelect(); expect(\"p\", \")\");\n")
	buf.WriteString("            fields.push({ name, subSelect: sel });\n")
	buf.WriteString("          } else if (peek().k === \"kw\" && peek().v === \"insert\") {\n")
	buf.WriteString("            eat(); const ins = parseInsert(); expect(\"p\", \")\");\n")
	buf.WriteString("            fields.push({ name, subInsert: ins });\n")
	buf.WriteString("          } else { throw new Error(\"axel/aql: expected select or insert in sub-expression\"); }\n")
	buf.WriteString("        } else if (peek().k === \"param\") {\n")
	buf.WriteString("          const p = eat();\n")
	buf.WriteString("          fields.push({ name, value: { kind: \"param\", v: p.v } });\n")
	buf.WriteString("        } else {\n")
	buf.WriteString("          const lit = eat();\n")
	buf.WriteString("          fields.push({ name, value: { kind: \"lit\", v: lit.v } });\n")
	buf.WriteString("        }\n")
	buf.WriteString("      } else {\n")
	buf.WriteString("        fields.push({ name });\n")
	buf.WriteString("      }\n")
	buf.WriteString("      skip(\"p\", \",\"); skip(\"p\", \";\");\n")
	buf.WriteString("    }\n")
	buf.WriteString("    expect(\"p\", \"}\");\n")
	buf.WriteString("    return fields;\n")
	buf.WriteString("  };\n\n")
	buf.WriteString("  parseSelect = (): _AQLSelect => {\n")
	buf.WriteString("    const typeName = expect(\"id\").v;\n")
	buf.WriteString("    const fields = parseFields();\n")
	buf.WriteString("    const filter = parseFilter();\n")
	buf.WriteString("    return { typeName, fields, filter };\n")
	buf.WriteString("  };\n\n")
	buf.WriteString("  parseInsert = (): _AQLInsert => {\n")
	buf.WriteString("    const typeName = expect(\"id\").v;\n")
	buf.WriteString("    const fields = parseFields();\n")
	buf.WriteString("    return { typeName, fields };\n")
	buf.WriteString("  };\n\n")
	buf.WriteString("  skip(\"p\", \"(\");\n")
	buf.WriteString("  const kw = eat();\n")
	buf.WriteString("  if (kw.k !== \"kw\") throw new Error(\"axel/aql: expected keyword, got \" + JSON.stringify(kw));\n")
	buf.WriteString("  if (kw.v === \"select\") return { kind: \"select\", sel: parseSelect() };\n")
	buf.WriteString("  if (kw.v === \"insert\") return { kind: \"insert\", ins: parseInsert() };\n")
	buf.WriteString("  if (kw.v === \"delete\") {\n")
	buf.WriteString("    const typeName = expect(\"id\").v;\n")
	buf.WriteString("    const filter = parseFilter();\n")
	buf.WriteString("    return { kind: \"delete\", del: { typeName, filter } };\n")
	buf.WriteString("  }\n")
	buf.WriteString("  throw new Error(\"axel/aql: unsupported statement: \" + kw.v);\n")
	buf.WriteString("}\n\n")

	// ─── AQL runtime: compiler ────────────────────────────────────────────────────
	buf.WriteString("// eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("type _SchemaTypes = Array<{\n")
	buf.WriteString("  name: string; table: string;\n")
	buf.WriteString("  properties?: Array<{ name: string; column: string }>;\n")
	buf.WriteString("  links?: Array<{ name: string; join_column: string }>;\n")
	buf.WriteString("}>;\n\n")
	buf.WriteString("function _compileSelectAST(\n")
	buf.WriteString("  types: _SchemaTypes,\n")
	buf.WriteString("  sel: _AQLSelect,\n")
	buf.WriteString("  paramIdx: { v: number },\n")
	buf.WriteString("): string {\n")
	buf.WriteString("  const type = types.find((t) => t.name === sel.typeName);\n")
	buf.WriteString("  if (!type) throw new Error(`axel: unknown type \"${sel.typeName}\"`);\n")
	buf.WriteString("  const alias = sel.typeName[0]!.toLowerCase();\n")
	buf.WriteString("  const cols: string[] = [];\n")
	buf.WriteString("  for (const f of sel.fields) {\n")
	buf.WriteString("    if (f.subSelect) {\n")
	buf.WriteString("      const innerSQL = _compileSelectAST(types, f.subSelect, paramIdx);\n")
	buf.WriteString("      const subAlias = `${f.subSelect.typeName[0]!.toLowerCase()}_${f.name}_sub`;\n")
	buf.WriteString("      cols.push(`(SELECT json_agg(row_to_json(${subAlias})) FROM (${innerSQL}) ${subAlias}) AS \"${f.name}\"`);\n")
	buf.WriteString("      continue;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    const col = _toSnakeCase(f.name);\n")
	buf.WriteString("    const prop = type.properties?.find((p) => p.name === col || p.column === col);\n")
	buf.WriteString("    if (prop) { cols.push(`${alias}.\"${prop.column}\"`); continue; }\n")
	buf.WriteString("    const link = type.links?.find((l) => l.name === col);\n")
	buf.WriteString("    if (link) { cols.push(`${alias}.\"${link.join_column}\"`); continue; }\n")
	buf.WriteString("    cols.push(`${alias}.\"${col}\"`);\n")
	buf.WriteString("  }\n")
	buf.WriteString("  if (cols.length === 0) cols.push(`${alias}.*`);\n\n")
	buf.WriteString("  const filterExprs: string[] = [];\n")
	buf.WriteString("  if (sel.filter) {\n")
	buf.WriteString("    const f = sel.filter;\n")
	buf.WriteString("    const parts = f.field.split(\".\");\n")
	buf.WriteString("    let fcol: string;\n")
	buf.WriteString("    if (parts.length >= 2) {\n")
	buf.WriteString("      const linkName = _toSnakeCase(parts[0]!);\n")
	buf.WriteString("      const lnk = type.links?.find((l) => l.name === linkName);\n")
	buf.WriteString("      fcol = lnk ? `${alias}.\"${lnk.join_column}\"` : `${alias}.\"${_toSnakeCase(f.field)}\"`;\n")
	buf.WriteString("    } else {\n")
	buf.WriteString("      const snakeF = _toSnakeCase(f.field);\n")
	buf.WriteString("      const prop2 = type.properties?.find((p) => p.name === snakeF || p.column === snakeF);\n")
	buf.WriteString("      const lnk2 = !prop2 && type.links?.find((l) => l.name === snakeF || l.join_column === snakeF || l.name + \"_id\" === snakeF);\n")
	buf.WriteString("      fcol = prop2 ? `${alias}.\"${prop2.column}\"` : lnk2 ? `${alias}.\"${lnk2.join_column}\"` : `${alias}.\"${snakeF}\"`;\n")
	buf.WriteString("    }\n")
	buf.WriteString("    if (f.isRef) {\n")
	buf.WriteString("      const refParts = f.value.split(\".\");\n")
	buf.WriteString("      let refCol = f.value;\n")
	buf.WriteString("      if (refParts.length === 2) {\n")
	buf.WriteString("        const refTypeName = refParts[0]!;\n")
	buf.WriteString("        const refField = _toSnakeCase(refParts[1]!);\n")
	buf.WriteString("        const refAlias = refTypeName[0]!.toLowerCase();\n")
	buf.WriteString("        const refType = types.find((t) => t.name === refTypeName);\n")
	buf.WriteString("        const refProp = refType?.properties?.find((p) => p.name === refField || p.column === refField);\n")
	buf.WriteString("        refCol = refProp ? `${refAlias}.\"${refProp.column}\"` : `${refAlias}.\"${refField}\"`;\n")
	buf.WriteString("      }\n")
	buf.WriteString("      filterExprs.push(`${fcol} ${f.op} ${refCol}`);\n")
	buf.WriteString("    } else {\n")
	buf.WriteString("      filterExprs.push(`${fcol} ${f.op} $${paramIdx.v++}`);\n")
	buf.WriteString("    }\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  let sql = `SELECT ${cols.join(\", \")} FROM \"${type.table}\" ${alias}`;\n")
	buf.WriteString("  if (filterExprs.length > 0) sql += ` WHERE ${filterExprs.join(\" AND \")}`;\n")
	buf.WriteString("  return sql;\n")
	buf.WriteString("}\n\n")
	buf.WriteString("function _compileAQL(\n")
	buf.WriteString("  schema: Record<string, unknown>,\n")
	buf.WriteString("  src: string,\n")
	buf.WriteString("  namedParams: Record<string, unknown>,\n")
	buf.WriteString("): { sql: string; params: unknown[] } {\n")
	buf.WriteString("  // eslint-disable-next-line @typescript-eslint/no-explicit-any\n")
	buf.WriteString("  const types = (schema as any).types as _SchemaTypes;\n")
	buf.WriteString("  const stmt = _parseAQL(src);\n")
	buf.WriteString("  const params: unknown[] = [];\n")
	buf.WriteString("  const paramIdx = { v: 1 };\n\n")
	buf.WriteString("  if (stmt.kind === \"select\") {\n")
	buf.WriteString("    const collectParams = (sel: _AQLSelect): void => {\n")
	buf.WriteString("      for (const f of sel.fields) { if (f.subSelect) collectParams(f.subSelect); }\n")
	buf.WriteString("      if (sel.filter && !sel.filter.isRef)\n")
	buf.WriteString("        params.push(sel.filter.isParam ? namedParams[sel.filter.value] : sel.filter.value);\n")
	buf.WriteString("    };\n")
	buf.WriteString("    collectParams(stmt.sel);\n")
	buf.WriteString("    const sql = _compileSelectAST(types, stmt.sel, paramIdx);\n")
	buf.WriteString("    return { sql, params };\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  if (stmt.kind === \"insert\") {\n")
	buf.WriteString("    const type = types.find((t) => t.name === stmt.ins.typeName);\n")
	buf.WriteString("    if (!type) throw new Error(`axel: unknown type \"${stmt.ins.typeName}\"`);\n")
	buf.WriteString("    const cols: string[] = [];\n")
	buf.WriteString("    for (const f of stmt.ins.fields) {\n")
	buf.WriteString("      if (!f.value) continue;\n")
	buf.WriteString("      const col = _toSnakeCase(f.name);\n")
	buf.WriteString("      const prop = type.properties?.find((p) => p.name === col || p.column === col);\n")
	buf.WriteString("      if (!prop) continue;\n")
	buf.WriteString("      cols.push(`\"${prop.column}\"`);\n")
	buf.WriteString("      if (f.value.kind === \"param\") params.push(namedParams[f.value.v]);\n")
	buf.WriteString("      else params.push(isNaN(Number(f.value.v)) ? f.value.v : Number(f.value.v));\n")
	buf.WriteString("    }\n")
	buf.WriteString("    const placeholders = params.map((_, i) => `$${i + 1}`);\n")
	buf.WriteString("    const sql = `INSERT INTO \"${type.table}\" (${cols.join(\", \")}) VALUES (${placeholders.join(\", \")}) RETURNING *`;\n")
	buf.WriteString("    return { sql, params };\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  if (stmt.kind === \"delete\") {\n")
	buf.WriteString("    const type = types.find((t) => t.name === stmt.del.typeName);\n")
	buf.WriteString("    if (!type) throw new Error(`axel: unknown type \"${stmt.del.typeName}\"`);\n")
	buf.WriteString("    const alias = stmt.del.typeName[0]!.toLowerCase();\n")
	buf.WriteString("    const filterExprs: string[] = [];\n")
	buf.WriteString("    if (stmt.del.filter) {\n")
	buf.WriteString("      const f = stmt.del.filter;\n")
	buf.WriteString("      const snakeF = _toSnakeCase(f.field);\n")
	buf.WriteString("      const prop = type.properties?.find((p) => p.name === snakeF || p.column === snakeF);\n")
	buf.WriteString("      const col = prop ? `${alias}.\"${prop.column}\"` : `${alias}.\"${snakeF}\"`;\n")
	buf.WriteString("      if (!f.isRef) {\n")
	buf.WriteString("        params.push(f.isParam ? namedParams[f.value] : f.value);\n")
	buf.WriteString("        filterExprs.push(`${col} ${f.op} $${paramIdx.v++}`);\n")
	buf.WriteString("      } else {\n")
	buf.WriteString("        filterExprs.push(`${col} ${f.op} ${f.value}`);\n")
	buf.WriteString("      }\n")
	buf.WriteString("    }\n")
	buf.WriteString("    let sql = `DELETE FROM \"${type.table}\" ${alias}`;\n")
	buf.WriteString("    if (filterExprs.length > 0) sql += ` WHERE ${filterExprs.join(\" AND \")}`;\n")
	buf.WriteString("    return { sql, params };\n")
	buf.WriteString("  }\n\n")
	buf.WriteString("  throw new Error(\"axel/aql: unhandled statement kind\");\n")
	buf.WriteString("}\n\n")

	// Queries class — holds all typed AQL convenience methods.
	buf.WriteString("export class Queries {\n")
	buf.WriteString("  constructor(private db: DB) {}\n\n")

	for _, q := range g.queries {
		fn := q.Name
		pt := toUpperFirst(q.Name) + "Params"
		rt := toUpperFirst(q.Name) + "Row"
		hp := len(q.Params) > 0

		switch q.Operation {
		case "select":
			if q.Result.IsScalar {
				if hp {
					fmt.Fprintf(&buf, "  %s(params: %s): Promise<number> {\n", fn, pt)
					fmt.Fprintf(&buf, "    return %s(this.db, params);\n  }\n\n", fn)
				} else {
					fmt.Fprintf(&buf, "  %s(): Promise<number> {\n", fn)
					fmt.Fprintf(&buf, "    return %s(this.db);\n  }\n\n", fn)
				}
			} else if q.Result.IsMultiple {
				if hp {
					fmt.Fprintf(&buf, "  %s(params: %s): Promise<%s[]> {\n", fn, pt, rt)
					fmt.Fprintf(&buf, "    return %s(this.db, params);\n  }\n\n", fn)
				} else {
					fmt.Fprintf(&buf, "  %s(): Promise<%s[]> {\n", fn, rt)
					fmt.Fprintf(&buf, "    return %s(this.db);\n  }\n\n", fn)
				}
			} else {
				if hp {
					fmt.Fprintf(&buf, "  %s(params: %s): Promise<%s | null> {\n", fn, pt, rt)
					fmt.Fprintf(&buf, "    return %s(this.db, params);\n  }\n\n", fn)
				} else {
					fmt.Fprintf(&buf, "  %s(): Promise<%s | null> {\n", fn, rt)
					fmt.Fprintf(&buf, "    return %s(this.db);\n  }\n\n", fn)
				}
			}
		case "insert", "update":
			if hp {
				fmt.Fprintf(&buf, "  %s(params: %s): Promise<%s | null> {\n", fn, pt, rt)
				fmt.Fprintf(&buf, "    return %s(this.db, params);\n  }\n\n", fn)
			} else {
				fmt.Fprintf(&buf, "  %s(): Promise<%s | null> {\n", fn, rt)
				fmt.Fprintf(&buf, "    return %s(this.db);\n  }\n\n", fn)
			}
		case "delete":
			if hp {
				fmt.Fprintf(&buf, "  %s(params: %s): Promise<void> {\n", fn, pt)
				fmt.Fprintf(&buf, "    return %s(this.db, params);\n  }\n\n", fn)
			} else {
				fmt.Fprintf(&buf, "  %s(): Promise<void> {\n", fn)
				fmt.Fprintf(&buf, "    return %s(this.db);\n  }\n\n", fn)
			}
		}
	}

	buf.WriteString("}\n\n")

	// Runner class — fluent builder methods + typed query namespace.
	buf.WriteString("export class Runner {\n")
	buf.WriteString("  public query: Queries;\n\n")
	buf.WriteString("  constructor(private db: DB) {\n")
	buf.WriteString("    this.query = new Queries(db);\n")
	buf.WriteString("  }\n\n")

	buf.WriteString("  select<K extends keyof AxelSchema, S extends Shape<AxelSchema[K]>>(\n")
	buf.WriteString("    type: K,\n")
	buf.WriteString("    shape: S,\n")
	buf.WriteString("  ): SelectBuilder<AxelSchema[K], S> {\n")
	buf.WriteString("    return new SelectBuilder(this.db, _schema, type as string, shape);\n")
	buf.WriteString("  }\n\n")

	buf.WriteString("  insert<K extends keyof AxelSchema>(\n")
	buf.WriteString("    type: K,\n")
	buf.WriteString("    values: Insertable<AxelSchema[K]>,\n")
	buf.WriteString("  ): InsertBuilder<AxelSchema[K]> {\n")
	buf.WriteString("    return new InsertBuilder(this.db, _schema, type as string, values);\n")
	buf.WriteString("  }\n\n")

	buf.WriteString("  async run<T = Record<string, unknown>>(\n")
	buf.WriteString("    aql: string,\n")
	buf.WriteString("    params: Record<string, unknown> = {},\n")
	buf.WriteString("  ): Promise<T[]> {\n")
	buf.WriteString("    const { sql, params: positional } = _compileAQL(_schema, aql, params);\n")
	buf.WriteString("    return this.db.unsafe<T>(sql, positional);\n")
	buf.WriteString("  }\n")

	buf.WriteString("}\n")
	return ctx.WriteFile("runner.ts", buf.Bytes())
}

// --- emit helpers ---

// emitTsFuncSig writes the function signature opening line.
func emitTsFuncSig(buf *bytes.Buffer, funcName string, hasParams bool, paramsType, returnType string) {
	if hasParams {
		fmt.Fprintf(buf, "export async function %s(db: DB, params: %s): %s {\n", funcName, paramsType, returnType)
	} else {
		fmt.Fprintf(buf, "export async function %s(db: DB): %s {\n", funcName, returnType)
	}
}

// emitTsRowInterface writes a TypeScript interface for a result shape, recursing into sub-fields.
func emitTsRowInterface(buf *bytes.Buffer, name string, fields []codegen.ResultField) {
	fmt.Fprintf(buf, "export interface %s {\n", name)
	for _, f := range fields {
		fieldName := toTsCamelCase(f.Name)
		if len(f.SubFields) > 0 {
			subType := name + toUpperFirst(fieldName)
			tsType := subType
			if f.IsMultiple {
				tsType = subType + "[]"
			}
			if f.IsNullable && !f.IsMultiple {
				tsType += " | null"
			}
			fmt.Fprintf(buf, "  %s: %s;\n", fieldName, tsType)
		} else {
			fmt.Fprintf(buf, "  %s: %s;\n", fieldName, aqlToTsType(f.AQLType, f.IsNullable))
		}
	}
	fmt.Fprintf(buf, "}\n\n")

	for _, f := range fields {
		if len(f.SubFields) > 0 {
			subType := name + toUpperFirst(toTsCamelCase(f.Name))
			emitTsRowInterface(buf, subType, f.SubFields)
		}
	}
}

// buildTsParamArgs returns the array elements for db.unsafe params, e.g. "params.email, params.name".
func buildTsParamArgs(params []codegen.ParamDescriptor) string {
	parts := make([]string, len(params))
	for i, p := range params {
		parts[i] = "params." + toTsCamelCase(p.Name)
	}
	return strings.Join(parts, ", ")
}

// --- type / name helpers ---

// aqlToTsType maps an AQL type name to the appropriate TypeScript type.
func aqlToTsType(aqlType string, nullable bool) string {
	base := map[string]string{
		"str":      "string",
		"uuid":     "string",
		"decimal":  "string",
		"int16":    "number",
		"int32":    "number",
		"int64":    "number",
		"float32":  "number",
		"float64":  "number",
		"bool":     "boolean",
		"datetime": "Date",
		"date":     "Date",
		"time":     "string",
		"json":     "unknown",
		"bytes":    "Uint8Array",
	}[aqlType]
	if base == "" {
		base = "unknown"
	}
	if nullable {
		return base + " | null"
	}
	return base
}

// toTsCamelCase converts snake_case to camelCase.
func toTsCamelCase(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if i == 0 || p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "")
}

// toUpperFirst capitalizes the first character of s.
func toUpperFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

// queryTsFileName derives the output .ts file base name from the query descriptor.
func queryTsFileName(q codegen.QueryDescriptor) string {
	if q.File != "" {
		base := filepath.Base(q.File)
		for ext := filepath.Ext(base); ext != ""; ext = filepath.Ext(base) {
			base = strings.TrimSuffix(base, ext)
		}
		return base
	}
	return tsSnakeCase(q.Name)
}

// tsSnakeCase converts camelCase to snake_case.
func tsSnakeCase(s string) string {
	var out strings.Builder
	for i, r := range s {
		if r >= 'A' && r <= 'Z' && i > 0 {
			out.WriteByte('_')
		}
		out.WriteRune(r | 0x20)
	}
	return out.String()
}

// escapeTsBacktick escapes backticks and backslashes so the string can be safely
// embedded in a TypeScript template literal.
func escapeTsBacktick(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "`", "\\`")
	return s
}
